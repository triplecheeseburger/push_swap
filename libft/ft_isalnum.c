#include <stdlib.h>

int	ft_isalnum(int c)
{
	if ('0' <= c && c <= '9')
		return (42);
	else if ('a' <= c && c <= 'z')
		return (42);
	else if ('A' <= c && c <= 'Z')
		return (42);
	return (0);
}
